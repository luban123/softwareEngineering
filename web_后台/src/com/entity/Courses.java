package com.entity;

public class Courses {
private String cid;
private String cname;
private String tid;
public String getCid() {
	return cid;
}
public void setCid(String cid) {
	this.cid = cid;
}
public String getCname() {
	return cname;
}
public void setCname(String cname) {
	this.cname = cname;
}
public String getTid() {
	return tid;
}
public void setTid(String tid) {
	this.tid = tid;
}


}
